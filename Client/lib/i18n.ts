export function localizeSpecialty(locale: Locale, raw: string): string {
  const key = `spec_${normalizeSpecialty(raw)}`
  const en = translations.en as Record<string, string>
  const loc = translations[locale] as Record<string, string>
  return (loc && loc[key]) || en[key] || raw
}

export function localizeDoctorName(locale: Locale, name: string): string {
  if (!name) return locale === "ar" ? "د. طبيب" : "Dr. Doctor"
  const base = name.replace(/^Dr\.?\s*|^د\.?\s*/i, "").trim()
  if (locale === "ar") {
    // Add LRM to keep Latin name order after Arabic prefix
    return `د.\u200E ${base}`
  }
  return `Dr. ${base}`
}

